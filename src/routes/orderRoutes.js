// routes/orderRoutes.js
const express = require('express');
const router = express.Router();
const authMiddleware = require('../middleware/authMiddleware');
const { Order, OrderItem, OrderStatusHistory, FoodPackage, Seller, User } = require('../models');

// ‚úÖ Sipari≈ü olu≈üturma endpoint'i
router.post('/create', authMiddleware, async (req, res) => {
  const transaction = await Order.sequelize.transaction();
  
  try {
    const { 
      cartItems, 
      totalAmount, 
      paymentMethod, 
      deliveryAddress,
      customerNotes,
      estimatedPickupTime 
    } = req.body;

    const userId = req.user.id;

    console.log('üõí Sipari≈ü olu≈üturma ba≈üladƒ±:', {
      userId,
      totalAmount,
      cartItemsCount: cartItems?.length || 0
    });

    // 1. Validasyonlar
    if (!cartItems || cartItems.length === 0) {
      await transaction.rollback();
      return res.status(400).json({
        success: false,
        message: 'Sepetinizde √ºr√ºn bulunmuyor'
      });
    }

    if (!totalAmount || totalAmount <= 0) {
      await transaction.rollback();
      return res.status(400).json({
        success: false,
        message: 'Ge√ßerli bir toplam tutar giriniz'
      });
    }

    if (!estimatedPickupTime) {
      await transaction.rollback();
      return res.status(400).json({
        success: false,
        message: 'Teslim alma zamanƒ± giriniz'
      });
    }

    // 2. Pickup date ve time'ƒ± ayƒ±r
    const pickupDateTime = new Date(estimatedPickupTime);
    const pickupDate = pickupDateTime.toISOString().split('T')[0]; // YYYY-MM-DD
    const pickupTime = pickupDateTime.toTimeString().split(' ')[0]; // HH:MM:SS

    // 3. Seller ID'yi belirle (ilk √ºr√ºn√ºn seller'ƒ±)
    const firstPackage = await FoodPackage.findByPk(cartItems[0].package_id);
    if (!firstPackage) {
      await transaction.rollback();
      return res.status(404).json({
        success: false,
        message: '√úr√ºn bulunamadƒ±'
      });
    }

    const sellerId = firstPackage.seller_id;

    // 4. Ana sipari≈üi olu≈ütur
    const order = await Order.create({
      user_id: userId,
      seller_id: sellerId,
      total_amount: totalAmount,
      order_status: 'pending',
      payment_status: 'pending',
      pickup_date: pickupDate,
      pickup_time: pickupTime,
      notes: customerNotes || null
    }, { transaction });

    console.log('‚úÖ Ana sipari≈ü olu≈üturuldu:', order.order_id);

    // 5. Sipari≈ü detaylarƒ±nƒ± olu≈ütur
    const orderItems = [];
    for (const item of cartItems) {
      const packageInfo = await FoodPackage.findByPk(item.package_id);
      
      if (!packageInfo) {
        console.warn(`‚ö†Ô∏è Package not found: ${item.package_id}`);
        continue;
      }

      const unitPrice = parseFloat(packageInfo.new_price || packageInfo.price || 0);
      const quantity = parseInt(item.quantity || 1);
      const totalPrice = unitPrice * quantity;

      const orderItem = await OrderItem.create({
        order_id: order.order_id,
        package_id: item.package_id,
        quantity: quantity,
        unit_price: unitPrice,
        total_price: totalPrice,
        item_status: 'ordered'
      }, { transaction });

      orderItems.push(orderItem);
      console.log(`‚úÖ OrderItem olu≈üturuldu: ${orderItem.item_id} (Package: ${item.package_id})`);
    }

    // 6. ƒ∞lk durum ge√ßmi≈üi kaydƒ±
    await OrderStatusHistory.create({
      order_id: order.order_id,
      old_status: null,
      new_status: 'pending',
      changed_by: userId,
      notes: 'Sipari≈ü olu≈üturuldu'
    }, { transaction });

    // 7. Transaction'ƒ± commit et
    await transaction.commit();

    // 8. √ñdeme i≈ülemini sim√ºle et
    let paymentSuccess = true;
    let paymentMessage = '√ñdeme ba≈üarƒ±lƒ±';

    // Ger√ßek √∂deme gateway entegrasyonu burada yapƒ±lacak
    if (paymentMethod === 'card') {
      // Kredi kartƒ± i≈ülemi
      paymentSuccess = Math.random() > 0.1; // %90 ba≈üarƒ± oranƒ±
    } else if (paymentMethod === 'paypal') {
      // PayPal i≈ülemi
      paymentSuccess = Math.random() > 0.05; // %95 ba≈üarƒ± oranƒ±
    }

    // 9. √ñdeme durumunu g√ºncelle
    if (paymentSuccess) {
      await order.update({
        payment_status: 'completed',
        order_status: 'confirmed'
      });

      // Durum ge√ßmi≈üi g√ºncelle
      await OrderStatusHistory.create({
        order_id: order.order_id,
        old_status: 'pending',
        new_status: 'confirmed',
        changed_by: userId,
        notes: '√ñdeme tamamlandƒ±'
      });

      console.log('‚úÖ √ñdeme ba≈üarƒ±lƒ±, sipari≈ü onaylandƒ±');
    } else {
      await order.update({
        payment_status: 'failed',
        order_status: 'cancelled'
      });

      paymentMessage = '√ñdeme i≈ülemi ba≈üarƒ±sƒ±z oldu';
      console.log('‚ùå √ñdeme ba≈üarƒ±sƒ±z, sipari≈ü iptal edildi');
    }

    // 10. Ba≈üarƒ±lƒ± response
    res.status(201).json({
      success: paymentSuccess,
      message: paymentSuccess ? 'Sipari≈üiniz ba≈üarƒ±yla olu≈üturuldu' : paymentMessage,
      orderId: order.order_id,
      trackingNumber: `SP${order.order_id.toString().padStart(6, '0')}`,
      orderStatus: order.order_status,
      paymentStatus: order.payment_status,
      totalAmount: order.total_amount,
      pickupDate: order.pickup_date,
      pickupTime: order.pickup_time,
      redirectUrl: '/orders' 
    });

  } catch (error) {
    await transaction.rollback();
    console.error('‚ùå Sipari≈ü olu≈üturma hatasƒ±:', error);
    
    res.status(500).json({
      success: false,
      message: 'Sipari≈ü olu≈üturulurken hata olu≈ütu',
      error: error.message
    });
  }
});

// ‚úÖ Kullanƒ±cƒ±nƒ±n sipari≈ülerini getirme
router.get('/my-orders', authMiddleware, async (req, res) => {
  try {
    const userId = req.user.id;
    const { page = 1, limit = 10, status } = req.query;

    console.log('üìã Sipari≈üler getiriliyor:', { userId, page, limit, status });

    const whereClause = { user_id: userId };
    if (status && status !== 'all') {
      whereClause.order_status = status;
    }

    const orders = await Order.findAndCountAll({
      where: whereClause,
      include: [
        {
          model: OrderItem,
          as: 'items',
          include: [
            {
              model: FoodPackage,
              as: 'package',
              attributes: ['package_id', 'title', 'description', 'price', 'new_price', 'image_url']
            }
          ]
        },
        {
          model: Seller,
          as: 'seller',
          attributes: ['seller_id', 'store_name', 'phone', 'address']
        }
      ],
      order: [['order_date', 'DESC']],
      limit: parseInt(limit),
      offset: (parseInt(page) - 1) * parseInt(limit)
    });

    // Veriyi frontend formatƒ±na d√∂n√º≈üt√ºr
    const formattedOrders = orders.rows.map(order => ({
      id: order.order_id,
      storeName: order.seller?.store_name || 'Bilinmeyen Maƒüaza',
      storeImage: 'https://via.placeholder.com/60', // Default image
      productName: order.items?.length > 0 ? order.items[0].package.title : '√úr√ºn',
      orderDate: order.order_date,
      pickupDate: `${order.pickup_date}T${order.pickup_time}`,
      address: order.seller?.address || 'Adres bilgisi yok',
      price: parseFloat(order.total_amount),
      originalPrice: parseFloat(order.total_amount) * 1.5, // √ñrnek indirim hesabƒ±
      status: mapOrderStatus(order.order_status),
      items: order.items?.map(item => ({
        name: item.package.title,
        quantity: item.quantity,
        price: parseFloat(item.total_price)
      })) || []
    }));

    res.json({
      success: true,
      orders: formattedOrders,
      totalCount: orders.count,
      currentPage: parseInt(page),
      totalPages: Math.ceil(orders.count / parseInt(limit))
    });

  } catch (error) {
    console.error('‚ùå Sipari≈üler getirme hatasƒ±:', error);
    res.status(500).json({
      success: false,
      message: 'Sipari≈üler getirilemedi',
      error: error.message
    });
  }
});

// ‚úÖ Sipari≈ü detayƒ±nƒ± getirme
router.get('/:orderId', authMiddleware, async (req, res) => {
  try {
    const { orderId } = req.params;
    const userId = req.user.id;

    const order = await Order.findOne({
      where: {
        order_id: orderId,
        user_id: userId
      },
      include: [
        {
          model: OrderItem,
          as: 'items',
          include: [
            {
              model: FoodPackage,
              as: 'package'
            }
          ]
        },
        {
          model: Seller,
          as: 'seller'
        },
        {
          model: OrderStatusHistory,
          as: 'statusHistory',
          order: [['changed_at', 'ASC']]
        }
      ]
    });

    if (!order) {
      return res.status(404).json({
        success: false,
        message: 'Sipari≈ü bulunamadƒ±'
      });
    }

    res.json({
      success: true,
      order: order
    });

  } catch (error) {
    console.error('‚ùå Sipari≈ü detay hatasƒ±:', error);
    res.status(500).json({
      success: false,
      message: 'Sipari≈ü detayƒ± getirilemedi',
      error: error.message
    });
  }
});

// ‚úÖ Sipari≈ü iptal etme
router.patch('/:orderId/cancel', authMiddleware, async (req, res) => {
  try {
    const { orderId } = req.params;
    const { reason } = req.body;
    const userId = req.user.id;

    const order = await Order.findOne({
      where: {
        order_id: orderId,
        user_id: userId
      }
    });

    if (!order) {
      return res.status(404).json({
        success: false,
        message: 'Sipari≈ü bulunamadƒ±'
      });
    }

    // Sadece pending veya confirmed sipari≈üler iptal edilebilir
    if (!['pending', 'confirmed'].includes(order.order_status)) {
      return res.status(400).json({
        success: false,
        message: 'Bu sipari≈ü iptal edilemez'
      });
    }

    const oldStatus = order.order_status;

    // Sipari≈üi iptal et
    await order.update({
      order_status: 'cancelled',
      cancellation_reason: reason || 'Kullanƒ±cƒ± tarafƒ±ndan iptal edildi'
    });

    // Sipari≈ü detaylarƒ±nƒ± da iptal et
    await OrderItem.update(
      { item_status: 'cancelled' },
      { where: { order_id: orderId } }
    );

    // Durum ge√ßmi≈üi ekle
    await OrderStatusHistory.create({
      order_id: orderId,
      old_status: oldStatus,
      new_status: 'cancelled',
      changed_by: userId,
      notes: reason || 'Kullanƒ±cƒ± tarafƒ±ndan iptal edildi'
    });

    res.json({
      success: true,
      message: 'Sipari≈ü ba≈üarƒ±yla iptal edildi'
    });

  } catch (error) {
    console.error('‚ùå Sipari≈ü iptal hatasƒ±:', error);
    res.status(500).json({
      success: false,
      message: 'Sipari≈ü iptal edilemedi',
      error: error.message
    });
  }
});

// ‚úÖ Yardƒ±mcƒ± fonksiyon: Order status'larƒ± frontend formatƒ±na √ßevir
function mapOrderStatus(backendStatus) {
  const statusMap = {
    'pending': 'devam_ediyor',
    'confirmed': 'devam_ediyor',
    'ready': 'hazir',
    'completed': 'teslim_edildi',
    'cancelled': 'iptal_edildi'
  };
  
  return statusMap[backendStatus] || 'devam_ediyor';
}

